<#
.SYNOPSIS
Detects available mirrors for OmniDocBench setup (China-firewall-aware).
Outputs mirrors.env with source URLs for all downstream scripts.
#>
$ErrorActionPreference = "Stop"
# NOTE: Join-Path is nested (rather than the PS 7+ 3-arg form) so this runs on
# Windows PowerShell 5.1 as well as PowerShell 7+.
$envFile = Join-Path (Join-Path $PSScriptRoot "..") "mirrors.env"
$resolvedEnvFile = (Resolve-Path (Join-Path $PSScriptRoot "..")).Path + "\mirrors.env"

function Test-Url($url, $timeoutSec = 8) {
    try {
        $r = Invoke-WebRequest -Uri $url -TimeoutSec $timeoutSec -UseBasicParsing -ErrorAction Stop
        return $r.StatusCode -lt 400
    } catch { return $false }
}

$lines = @("# Auto-generated by detect-mirrors.ps1 — do not edit")

# HuggingFace vs ModelScope
$hf = Test-Url "https://huggingface.co"
$ms = Test-Url "https://modelscope.cn"
if ($hf) {
    $lines += "HF_OR_MS=huggingface"
    $lines += "DATASET_URL=https://huggingface.co/datasets/opendatalab/OmniDocBench"
    $lines += "VLM_MODEL_URL=https://huggingface.co/PaddlePaddle/PaddleOCR-VL-1.6-GGUF"
    $lines += "LAYOUT_MODEL_URL=https://huggingface.co/AlexTransformer/PP-DocLayoutV3-onnx"
} elseif ($ms) {
    $lines += "HF_OR_MS=modelscope"
    $lines += "DATASET_URL=modelscope://OpenDataLab/OmniDocBench"
    $lines += "VLM_MODEL_URL=modelscope://PaddlePaddle/PaddleOCR-VL-1.6-GGUF"
    $lines += "LAYOUT_MODEL_URL=modelscope://AlexTransformer/PP-DocLayoutV3-onnx"
} else {
    Write-Host "ERROR: Neither HuggingFace nor ModelScope is reachable." -ForegroundColor Red
    exit 1
}

# GitHub vs gitclone/ghproxy
$gh = Test-Url "https://github.com/opendatalab/OmniDocBench"
if ($gh) {
    $lines += "GITHUB_BASE=https://github.com"
    $lines += "GITHUB_PROXY="
} else {
    foreach ($proxy in @("https://ghproxy.net", "https://ghfast.top")) {
        if (Test-Url "$proxy/https://github.com") {
            $lines += "GITHUB_BASE=$proxy/https://github.com"
            $lines += "GITHUB_PROXY=$proxy"
            break
        }
    }
    if (-not $lines.Contains("GITHUB_BASE=$proxy/https://github.com")) {
        Write-Host "ERROR: No GitHub access (direct or proxy)." -ForegroundColor Red
        exit 1
    }
}

# CTAN (TeX Live)
$tug = Test-Url "https://tug.org/texlive/"
if ($tug) {
    $lines += "CTAN_MIRROR=https://mirror.ctan.org/systems/texlive/tlnet"
} else {
    foreach ($mirror in @("https://mirrors.ustc.edu.cn/CTAN/systems/texlive/tlnet", "https://mirrors.tuna.tsinghua.edu.cn/CTAN/systems/texlive/tlnet")) {
        if (Test-Url $mirror) { $lines += "CTAN_MIRROR=$mirror"; break }
    }
}

# PyPI
$pypi = Test-Url "https://pypi.org/simple/"
if ($pypi) {
    $lines += "PYPI_INDEX=https://pypi.org/simple"
} else {
    $lines += "PYPI_INDEX=https://pypi.tuna.tsinghua.edu.cn/simple"
}

# WSL rootfs (Ubuntu base)
$lines += "UBUNTU_ROOTFS=https://mirrors.ustc.edu.cn/ubuntu-cdimage/ubuntu-base/releases/22.04/release/ubuntu-base-22.04.5-base-amd64.tar.gz"

[System.IO.File]::WriteAllLines($resolvedEnvFile, $lines)
Write-Host "mirrors.env written to $resolvedEnvFile" -ForegroundColor Green
Write-Host "Sources: $($lines[1]) | $($lines | Select-String 'GITHUB_BASE') | $($lines | Select-String 'CTAN_MIRROR')"
exit 0
